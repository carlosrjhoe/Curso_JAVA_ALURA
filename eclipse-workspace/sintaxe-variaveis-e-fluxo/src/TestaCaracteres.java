
public class TestaCaracteres {
	public static void main(String[] args) {
		
		char letra = 'a'; // Para mostrar apenas 1 letra
		System.out.println(letra);
		
		char valor = 66;
		System.out.println(valor);
		
		valor = (char) (valor + 1);
		System.out.println(valor);
		
		String palavra = "Carlos Roberto Conceição Júnior"; // Para mostrar palavras com mais de uma letra
		System.out.println("Meu nome é " + palavra);
	}
}
